public class MethodReturnTypes {
    public static void main(String[] args) {
        System.out.println(stringMeAlong());
        System.out.println(intersNeverWin());
        System.out.println(truesClues());
    }
    public static String stringMeAlong() {
        return ("Not so fast, villain!");
    }
    public static int intersNeverWin() {
        return (42);
    }
    public static boolean truesClues() {
        return (true);
    }
}
